package pack1;

public class Operators {
	
	
	
	public static void main(String[] args) {
		
		
		int x = 60;
		System.out.println(x);
		
//		i.	Pre-Incremnt: 
//		First value of x will be incremented by 1 and then assigned to y
		
		int y = ++x;
		System.out.println(y);
		
		System.out.println();
		System.out.println("Post-Incremnt: ");
		System.out.println();
		
//		ii.	Post-Increment: 
//		First value of a will be assigned to b and then "a" value will be incremented by 1
		
		int a = 40;
		System.out.println(a);
		
		int b = a++;
		System.out.println(b);
		System.out.println(a);
		
		
		
		
		System.out.println();
		System.out.println("Pre-Decrement ");
		System.out.println();
		
//		iii.	Pre-Decrement
		
//		First value of p will be decreased by 1 and then assigned to q
		
		
		int p = 80;
		
		int q = --p;
		System.out.println(q);
		
		
		
		System.out.println();
		System.out.println("Post-Decrement ");
		System.out.println();
		
		
//		iv.	Post- Decrement
//		First value of r will be assigned to s and then "r" value will be incremented by 1
		
		
		int r = 30;
		int s = r--;
		
		System.out.println(s);
		System.out.println(r);
		
	}
	
	
	
	
	
	
	

}
